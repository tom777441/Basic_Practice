
public class Promotion_Casting {
	public static void main(String[] args) {
	int a = 5 / 2;
	System.out.println(a);
	double b = 5 / 2;
	System.out.println(b);
	double c = (double) 5 / 2; //Casting
	System.out.println(c);
	double d = 5.0/2;   //Promotion
	System.out.println(d);
	double f = 5/2.0;
	System.out.print(f);
	}
}
